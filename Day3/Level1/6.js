let myDate = new Date();

console.log(`Year : ${myDate.getFullYear()}`);
console.log(`Month : ${myDate.getMonth()}`);
console.log(`Date : ${myDate.getDate()}`);
console.log(`Day : ${myDate.getDay()}`);
console.log(`Hours : ${myDate.getHours()}`);
console.log(`Minutes : ${myDate.getMinutes()}`);
